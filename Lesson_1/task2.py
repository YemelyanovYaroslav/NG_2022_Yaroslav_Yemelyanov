num_1 = int(input("Enter the first number: "))
num_2 = int(input("Enter the second number: "))
operator = input("Select an operation: ")

if operator == '+':
    {
        print(num_1 + num_2)
    }

if operator == '-':
    {
        print(num_1 - num_2)
    }

if operator == '*':
    {
        print(num_1 * num_2)
    }

if operator == '/':
    {
        print(num_1 / num_2)
    }
